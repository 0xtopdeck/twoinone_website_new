// One-off: bake a real Earth land/sea mask into a compact bit-packed base64
// string the Planet samples by lat/lon. Source: world-atlas land-110m
// (Natural Earth), tested with d3-geo's spherical point-in-polygon.
import { readFileSync, writeFileSync } from "node:fs";
import * as topojson from "topojson-client";
import * as d3geo from "d3-geo";

const root = new URL("../", import.meta.url);
const topo = JSON.parse(
  readFileSync(new URL("node_modules/world-atlas/land-110m.json", root), "utf8")
);
const land = topojson.feature(topo, topo.objects.land);
const geoContains = d3geo.geoContains;

const W = 256;
const H = 128;
const bits = new Uint8Array(Math.ceil((W * H) / 8));
let idx = 0;
let landCount = 0;
for (let iy = 0; iy < H; iy++) {
  const lat = 90 - ((iy + 0.5) / H) * 180;
  for (let ix = 0; ix < W; ix++) {
    const lon = -180 + ((ix + 0.5) / W) * 360;
    if (geoContains(land, [lon, lat])) {
      bits[idx >> 3] |= 1 << (idx & 7);
      landCount++;
    }
    idx++;
  }
}

const b64 = Buffer.from(bits).toString("base64");
const out = `// AUTO-GENERATED by scripts/gen-landmask.mjs — do not edit by hand.
// Earth land mask ${W}x${H}, bit-packed (1 = land), from world-atlas land-110m via d3-geo.
export const LAND_W = ${W};
export const LAND_H = ${H};
export const LAND_B64 =
  "${b64}";
`;
writeFileSync(new URL("lib/landmask.ts", root), out);
console.log(`land mask ${W}x${H}: ${landCount}/${W * H} land cells, ${bits.length} bytes, base64 ${b64.length} chars`);
